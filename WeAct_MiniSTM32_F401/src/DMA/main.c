volatile uint32_t time = 0;

void Systick_Callback(void) {

	if(time > 0) time--;
	return;
}

void Delay(uint32_t D)
{
	time = D;
	while(time != 0);
	return;
}


void DMA2_Stream0_Callback(void)
{

	//Logging and Error handling mechanisms will be added later
	if(IS_DMAn_S0_DIR_ERR(DMA2))
	{

	}
	if(IS_DMAn_S0_FIFO_ERR(DMA2))
	{
		SHINE_LED(YELLOW_1);
		CLEAR_DMAn_S0_FIFO_ERR(DMA2);
	}
	if(IS_DMAn_S0_TRNSF_ERR(DMA2))
	{
		SHINE_LED(YELLOW_2);
		CLEAR_DMAn_S0_TRNSF_ERR(DMA2);
	}
	if(IS_DMAn_S0_TRNSF_HLF(DMA2))
	{
		SHINE_LED(BLUE_1);
		CLEAR_DMAn_S0_TRNSF_HLF(DMA2);
	}
	if(IS_DMAn_S0_TRNSF_CMPLT(DMA2))
	{
		TOGGLE_LED(BLUE_1);
		TOGGLE_LED(BLUE_2);
		Delay(100);
		TOGGLE_LED(BLUE_1);
		TOGGLE_LED(BLUE_2);
		Delay(100);
		TOGGLE_LED(BLUE_1);
		TOGGLE_LED(BLUE_2);
		Delay(100);
		CLEAR_DMAn_S0_TRNSF_CMPLT(DMA2);
	}
}

